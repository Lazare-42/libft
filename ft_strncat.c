char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 1;
	j = 0;
	while (dest[i - 1])
		i++;
	while (src[j] && nb > 0)
	{
		dest[i - 1] = src[j];
		i++;
		j++;
		nb--;
	}
	return (dest);
}
