char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	if (src[i] == '\0')
		dest[i] = src[i];
	while (dest[i])
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
